            <span class="section-heading-upper">Sobre a</span>
            <span class="section-heading-uper">Empresa</span>
        </h2>
        <p class="mb-3">Cadastrada junto ao Cadastur, nº 24.087705.75.0001-5 (Sistema de cadastro de pessoas físicas e jurídicas que atuam no setor de turismo), FAJU surgiu no ano de 2015, com o objetivo de estimular o turismo regional e salientar os potenciais em atrativos turísticos.
            A empresa está focada no turismo receptivo, fazendo toda a organização do passeio, desde deslocamento entre visitações, alimentação e contatos gerais. Inclui ainda em sua oferta da prestação de serviço de turismo emissivo.
            FAJU preza sempre pelo bom atendimento com um serviço de qualidade, buscando sempre atender as expectativas dos turistas. 

