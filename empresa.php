        <p class="mb-3">O intuito destes passeios turísticos é apresentar o desenvolvimento econômico, cultural e as belezas naturais que potencializam o município de Itapiranga - SC e região. Os roteiros foram criados ainda para uma maior divulgação dos atrativos turísticos disponíveis.
            Os passeios são elaborados para grupos, sendo o número mínimo entre 15 e 20 pessoas não possuindo limite máximo. Os roteiros podem sofrer alterações e ainda serem modificados de acordo com o perfil do turista ou seu interesse nos pontos de visitação.
            Vale ressaltar ainda que toda organização ficará sempre sob responsabilidade de profissional qualificado.

